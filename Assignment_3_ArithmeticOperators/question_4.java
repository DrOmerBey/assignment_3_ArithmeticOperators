package Assignment_3_ArithmeticOperators;

public class question_4 {
	
	public static void main(String[] args) {
		
		int num1 = 50;
		int num2 = 90;
		int temp = 0;
		System.out.println("n1 = " + num1);
		System.out.println("n2 = " + num2);
		System.out.println("-------");
		
		temp=num1;
		num1=num2;
		num2=temp;
		
		System.out.println("n1 = " + num1);
		System.out.println("n2 = " + num2);
	}
	
}
